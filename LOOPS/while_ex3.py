
# you have $40 in your phone account
#after each call you will be charged $5
#print "how many call you can make"
#how much money left after each call


money_in_acc=40
each_call=5
call_number=1
while money_in_acc>= each_call:
    print(f"{call_number}")
    call_number+=1
    money_in_acc=money_in_acc-each_call
    print(f"after the call i have ${money_in_acc} left")


























