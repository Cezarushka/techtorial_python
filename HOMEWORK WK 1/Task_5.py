
#Read numbers in inches, convert to meters

num=2000.0
inch_to_cm=2.54
meter_to_cm= 100
cm_to_in=num * inch_to_cm
print(cm_to_in) # 5080
meter_to_in=cm_to_in/meter_to_cm
print(meter_to_in) # 50.8
print("2000.0 inches are",meter_to_in, "meters")
# 2000.0 inch is 50.8 meters


























